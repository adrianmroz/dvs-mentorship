<p class="footer">
			Link rot refers to the modern nuisance when links to a resource or webpage no longer work or become unreachable. All websites are affected by link rot, 
and this analysis focused on the NYT website. 
	</p>
	<p class="footer">
The dataset consisted of 553,693 articles, representing 1,627,612 unique links. 72% of the links were deep links 
that included a path to a specific page, and those were the ones examined.
